package dja33.msc.ukc.msc_log.sample;

import java.util.Map;
import java.util.TreeMap;

import dja33.msc.ukc.msc_log.MainActivity;

/**
 * Created by Dante on 26/02/2017.
 */

public class SampleHandler {

    private Map<String, Sample> samples = new TreeMap<>();

    private static final SampleHandler sm = new SampleHandler();

    public static final SampleHandler getHandler(){return sm;}

    private SampleHandler(){
        for(String key : MainActivity.keyboardKeys){
            samples.put(key, new Sample());
        }
    }

    public String getSample(String key){
        return samples.get(key).getSample(key);
    }

    public void addAccelerometerSample(String key, float x, float y, float z, float speed){
        samples.get(key).addAccelerometerSample(x,y,z,speed);
    }

    public void addAmplitudeSample(String key, double average){
        samples.get(key).addAmplitudeSample(average);
    }

    public void addFrequencySample(String key){
        samples.get(key).addFrequencySample();
    }
}
